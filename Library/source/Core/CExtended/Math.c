Fahrenheit Celsius_GetFahrenheit
(
	Celsius Number
)
{
	return ((Number * 9.0/5.0) + 32.0);
}

Celsius Fahrenheit_GetCelsius
(
	Fahrenheit Number
)
{
	return ((Number - 32) * 5.0/9.0);
}
